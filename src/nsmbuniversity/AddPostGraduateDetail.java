package nsmbuniversity;

/**
 *
 * @author Tharindu dilshan
 */
public class AddPostGraduateDetail {
    private String fullName;
    private String dob;
    private int age;
    private String address;
    private String qualificationType;
    private String institute;
    private int yearOfCompletion;

    /**
     * @return the fullName
     */
    public String getFullName() {
        return fullName;
    }

    /**
     * @param fullName the fullName to set
     */
    public void setFullName(String fullName) {
        this.fullName = fullName;
    }

    /**
     * @return the dob
     */
    public String getDob() {
        return dob;
    }

    /**
     * @param dob the dob to set
     */
    public void setDob(String dob) {
        this.dob = dob;
    }

    /**
     * @return the age
     */
    public int getAge() {
        return age;
    }

    /**
     * @param age the age to set
     */
    public void setAge(int age) {
        this.age = age;
    }

    /**
     * @return the address
     */
    public String getAddress() {
        return address;
    }

    /**
     * @param address the address to set
     */
    public void setAddress(String address) {
        this.address = address;
    }

    /**
     * @return the qualificationType
     */
    public String getQualificationType() {
        return qualificationType;
    }

    /**
     * @param qualificationType the qualificationType to set
     */
    public void setQualificationType(String qualificationType) {
        this.qualificationType = qualificationType;
    }

    /**
     * @return the institute
     */
    public String getInstitute() {
        return institute;
    }

    /**
     * @param institute the institute to set
     */
    public void setInstitute(String institute) {
        this.institute = institute;
    }

    /**
     * @return the yearOfCompletion
     */
    public int getYearOfCompletion() {
        return yearOfCompletion;
    }

    /**
     * @param yearOfCompletion the yearOfCompletion to set
     */
    public void setYearOfCompletion(int yearOfCompletion) {
        this.yearOfCompletion = yearOfCompletion;
    }
}
