    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        AddInstructorDetail aid=new AddInstructorDetail();
        try{
            aid.setFullName(txtFullName.getText());
            aid.setInstructorId(Integer.parseInt(txtInstructorId.getText()));
            aid.setFaculty(ddFaculty.getSelectedItem().toString());
            aid.setAge(Integer.parseInt(txtAge1.getText()));
            aid.setNIC(txtNIC.getText());
            aid.setQualificationType(txtQualificationType.getText());
            dbops.instructors(aid);
            
        }catch(Exception e){
            System.out.println(e);
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

