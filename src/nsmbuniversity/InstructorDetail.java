    private void txtNICKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNICKeyTyped
         char a=evt.getKeyChar();
        if(!Character.isDigit(a)){
            evt.consume();
        }
        else{
            if(txtNIC.getText().length()==9){
                evt.consume();
            }
        }
    }//GEN-LAST:event_txtNICKeyTyped

